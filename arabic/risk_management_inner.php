        <p class="content_para">تقوم المجموعة باستكمال مراحل برنامج احتواء الكوارث، الذي يستهدف أهم أنظمة وتطبيقات البنية التحتية، حيث طرحت المجموعة مشروعات إضافية لتعزيز نظم احتواء الكوارث لضمان استمرارية الأعمال والخدمات أخذا في الاعتبار مختلف الاحتمالات. ويقوم فريق الاستجابة للطوارئ بالإشراف على أعمال خطط الطوارئ الرئيسة، وإجراء التجارب الافتراضية على نحو دوري.</p>

        <h3 class="colr_purple marginb40  fontBold">خطط درء المخاطر</h3>
        <p class="content_para">تستمر المجموعة في تعزيز وحدة الأمن السيبراني، ويتم تطوير الأنظمة والسياسات الداخلية، ورفع مستويات إجراءات الأمان، وتكثيف التوعية، كما يتم اختبار فعالية خطط أمن المعلومات. </p>

        <h3 class="colr_purple marginb40  fontBold">التطورات التنظيمية: </h3>
        <p class="content_para">تتسم البيئة التنظيمية في أماكن عمل المجموعة بالتطور المستمر، وتؤثر على نحو مباشر على أعمالها، ومع تعدد الجهات التشريعية تتجدد التحديات والفرص. ويتوجب التعامل معها والالتزام بها على نحو يحافظ على مكتسبات المجموعة ومصالح ذوي العلاقة.</p>
        <p class="content_para">تقوم المجموعة بدراسة التشريعات التنظيمية على نحو مستمر، وتنسق الجهود مع القطاعات ذات العلاقة بها في داخل المجموعة وخارجها، وتطبق أفضل المعايير لضمان تقديم أفضل الخدمات لعملائها على نحو يحقق أهداف الخطط الوطنية. كما تم اعتماد تنظيم إداري جديد لقطاع الشؤون التنظيمية ليساهم في تعزيز قدرات المجموعة في المجال التنظيمي.</p>

        <h3 class="colr_purple marginb40  fontBold">اضطرابات سلسلة التوريد:</h3>
        <p class="content_para">تتعامل المجموعة مع عدد كبير من مزودي الخدمات، ويواكب ذلك خطر الاعتماد على مزود واحد لتنفيذ أعمال أساسية في المجموعة.</p>
        <p class="content_para">تقوم المجموعة بتنويع سلاسل الإمدادات بحيث لا تعتمد على أعداد محدودة من الموردين، كما تؤكد في عقودها على أن تكون الأنظمة متوافقة مع بعضها مهما اختلف المورد. كما تراجع أوضاع الشركات المتعاقد معها، وتضع تشريعات للتأكد من ملاءتها المالية والتشغيلية لمتطلبات المجموعة، بما يواكب التطور التقني وسلامة خطط أعمالها. </p>

        <h3 class="colr_purple marginb40  fontBold">-المخاطر المالية:</h3>
        <h3 class="colr_purple marginb40  fontBold">إدارة مخاطر الائتمان</h3>
        <p class="content_para">لدى المجموعة سياسات وارشادات معتمدة تسمح فقط بالتعامل مع أطراف أُخرى ذات تصنيف ائتماني مرتفع وتضع حدود بمقدار التعرض للمخاطر لكل طرف. تسمح سياسات وإرشادات المجموعة بالاستثمار فقط لدى أطراف أُخرى ذات تصنيف ائتماني جيد صادرة عن وكالات تصنيف ائتمان عالمية وتقيد التعرض للمخاطر لكل جهة على أن لا تتعدى نسبة التعرض عن 30% من حقوق ملكية الشركاء في الطرف الآخر. تتم مراقبة مخاطر الإئتمان الخاصة للمجموعة بشكل ربع سنوي. با ستثناء تركيز مخاطر الائتمان المذكور في إيضاح 11 في القوائم المالية السنوية، فإن تركيز المخاطر المتعلقة بالمدينين التجاريين يعتبر محدود لأن عملاء المجموعة يتكونون من عدد كبير من عملاء غير ذوي العلاقة. يتم وضع شروط الدفع والحد الائتماني بالتوافق مع معايير قطاع الاتصالات.</p>
        <p class="content_para"> يتم إجراء تقييم مستمر للوضع المالي للمدينين التجاريين وتعتقد الإدارة عدم الحاجة لتكوين مخصص مخاطر ائتمان زيادة عن المخصص الحالي للإنخفاض في قيمة هذه الأرصدة )للمزيد من التفاصيل، أنظر إيضاح رقم 11 في القوائم المالية السنوية).</p>
        <p class="content_para"> بالإضافة إلى ذلك، تتعرض المجموعة لمخاطر الائتمان بشأن الضمانات المالية المقدمة فيما يتعلق بترتيبات التمويل لبعض الشركات التابعة. إن الحد الأعلى لتعرض المجموعة لهذه المخاطر هو المبلغ الأعلى الذي قد يتعين على المجموعة سداده عند استحقاق الضمان. لا يوجد أي مؤشر أو واقعة تدل على أن المجموعة ستتكبد أي خسائر بشأن ضماناتها المالية بتاريخ إعداد القوائم المالية الموحدة.</p>

        <h3 class="colr_purple marginb40  fontBold">إدارة مخاطر العملات الأجنبية</h3>
        <p class="content_para">يعتبر الريال السعودي العملة الوظيفية للمجموعة والمثبت سعر صرفه  مقابل الدولار الأمريكي.وعليه، فإن المجموعة معرضة فقط لتقلبات أسعار الصرف من العمليات بالعملات الأجنبية الأُخرى عدا الدولار الأمريكي. ولذا فإن خطر تقلب العملات الاجنبية منخفض على مستوى المجموعة.</p>


        <h3 class="colr_purple marginb40  fontBold">إدارة مخاطر السيولة</h3>
        <p class="content_para">وضعت المجموعة إطار عمل شامل لإدارة مخاطر السيولة من أجل الوفاء بمتطلبات التمويل والسيولة القصيرة والمتوسطة والطويلة الأجل للمجموعة على ضوء التعليمات المعتمدة. <br>
        تقوم المجموعة بالتأكد من توفر السيولة عن طريق الاحتفاظ بإحتياطيات نقدية وإستثمارات قصيرة الأجل وتسهيلات ائتمانية تلتزم بها وغير مسحوبة من بنوك محلية ودولية ذات تصنيف ائتماني عالي. تحدد المجموعة متطلبات سيولتها عن طريق المراقبة المستمرة لتوقعات النقد قصيرة وطويلة الأجل بالمقارنة مع التدفقات النقدية الفعلية. يتم مراجعة موقف السيولة بالمجموعة بشكل دوري ويتم إجراء اختبارات الضغط باستخدام عدة إفتراضات متعلقة بالمصاريف الرأسمالية، توزيعات الأرباح، تحصيل المدينين التجاريين، وسداد القروض بدون إعادة التمويل.
