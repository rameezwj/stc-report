        <h3 class="colr_purple marginb40  fontBold">الخطر</h3>
        <h3 class="colr_purple marginb40  fontBold">الهجمات السيبرانية:</h3>
        <p class="content_para">الهجمات السيبرانية على القطاعات الحيوية ومنها قطاع الاتصالات وتقنية المعلومات تزداد احتماليتها على ضوء المستجدات الإقليمية والدولية، وقد  تؤدي إلى تعطيل خدمات الاتصالات وتقنية المعلومات، تعرض البيانات للاختراق.</p>

        <h3 class="colr_purple marginb40  fontBold">استمرارية الأعمال واحتواء الكوارث:</h3>
