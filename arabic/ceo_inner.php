            <br /> <br />
            وتوجتstc  خلال العام تعاونها مع شركائها العالميين من أجل تحسين منظومة خدمات الجيل الخامس والتقنيات الجديدة بأول اطلاق تجاري للجيل الخامس في المملكة العربية السعودية والكويت والبحرين.
