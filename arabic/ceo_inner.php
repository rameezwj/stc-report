            يُعد عام 2019 عاماً فارقاً في تاريخ المجموعة، إذ حققت شركة الاتصالات السعودية (stc) قفزات تنموية، حتى أضحت الممكّن الرقمي الرائد للقطاعين العام والخاص محلياً وإقليمياً، الذي يعد أحد الأهداف الرئيسة لرؤية المملكة العربية السعودية 2030، وطورت البيانات الضخمة والحوسبة السحابية والأمن السيبراني وعززت مفهوم إنترنت الأشياء والذكاء الاصطناعي، ودعمت المواهب السعودية في مجال التقنية والابتكار، بالإضافة إلى ارتفاع النتائج المالية مقارنة بالعام الماضي، مدعومةً بارتفاع إيرادات قطاع المستهلك والنواقل والمشغلين والشركات التابعة للمجموعة.
            <br /> <br />
            وشهد عام 2019م إطلاق المجموعة سمتها التجارية الجديدة الموحدة (stc) في السعودية والكويت والبحرين لتعزيز أدائها ودورها الرائد كممكّن رقمي رائد في المنطقة، وإبراز دورها في إثراء حياة عملائها من خلال تقديم خدمات الاتصالات المبتكرة وتقديم المنتجات والخدمات التي تمكن قطاع الأعمال من العمل بفعالية واقتصادية أعلى، إذ تمثل السمة الجديدة بدء انطلاقة مستقبلية جديدة لمسيرةstc  التي شكل تأسيسها في العام 1998م حدثاً مفصلياً لقطاع الاتصالات والتكنولوجيا وسجلت خلال 21 عاماً إحدى أهم قصص النجاح لمشاريع الخصخصة والتوسع في المملكة العربية السعودية والمنطقة. 
            <br /> <br />
            وتعزيزاً لمكانة stc المالية وبرهنة على نجاح خططها الاستثمارية في عصر التقنيات والخدمات الرقمية، فقد حققت نجاحاً في استثمارها في شركة كريم مع إعلان استحواذ شركة أوبر عليها بنحو 3.1 مليار دولار، حيث تبلغ حصة المجموعة منها نحو 8.8٪، بقيمة 274 مليون دولار، وستواصل المجموعة أداء دورها كحاضنة للمشاريع الإبداعية والأفكار الخلاقة المتوائمة مع التغيرات المتسارعة في الصناعة ومع التغير في أسلوب الحياة للعملاء. 
            <br /> <br />
            وحققت شركة stc pay نجاحاً كبيراً على مستوى المدفوعات الرقمية في المملكة، فبعد مرور عام على إطلاقها أصبحت من أفضل الوسائل من حيث الجودة والتكلفة لتحويل الأموال والخدمات المالية، وتقدم خدماتها في أكثر من 30 ألف نقطة بيع في المملكة، ونتعامل حالياً مع أكثر من 525 ألف وكيل معتمد لدى ويسترن يونيون في جميع أنحاء العالم.
            <br /> <br />
