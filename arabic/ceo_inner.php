            <br /> <br />
            وتأكيداً على ريادة المجموعة فقد تصدرت علامتها التجارية قائمة أغلى 50 علامة تجارية سعودية في عام 2019م وفقا لمؤسسة براندد فايننس إذ بلغت قيمتها 7.095 مليار دولار بارتفاع 6.7% مقارنة بالعام الماضي، مما يعكس تميز العلامة التجارية لـstc ومكانتها الرائدة، ونالت سبع جوائز في مهرجان "ترانزفورم أووردز" الشرق الأوسط وشمال إفريقيا الخاص بتطوير العلامة التجارية بدبي. 
