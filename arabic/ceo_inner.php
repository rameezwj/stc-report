            <br /> <br />
            وتماشياً مع دور stc المحوري في تمكين التحول الرقمي، وتطوير قطاع الاتصالات وتقنية المعلومات بالمملكة والتزامها بإثراء حياة عملائها عبر توفيرها حلولاً رقمية مبتكرة وأحدث التقنيات اللاسلكية للاتصال بالإنترنت، مع سرعة إرسال البيانات واستقبالها وتنزيلها بسرعة أكبر، فقد وفرت خدمات الجيل الخامس لزوار وحضور العديد من الفعاليات التي نظمتها المملكة ومنها مؤتمر مبادرة مستقبل الاستثمار (دافوس الصحراء)، وسباقات الفورميلا إي، ومواقع فعاليات مواسم السعودية.
